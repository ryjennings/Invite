import UIKit

extension UIColor
{
    class func inviteTitleColor() -> UIColor {
        return UIColor(red: 0.26, green: 0.40, blue: 0.50, alpha: 1.0)
    }
    
    class func inviteBodyColor() -> UIColor {
        return UIColor(red: 0.60, green: 0.72, blue: 0.80, alpha: 1.0)
    }
}
